# oneliner if else expression
num = 5
print("Positive" if num > 0 else "Negative")

print("EVEN" if num % 2 == 0 else "ODD")

a = 6
b = 7
print(a if a > b else b)  # prints 7


age = 35
print("Adult" if age >= 18 else "Child")  # prints Adult


temp = 30
print("Hot" if temp > 25 else "Cold")  # prints Cold