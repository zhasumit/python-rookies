# Arithmetic and math
friends = 10

friends = friends + 1
print(friends)  # 11


friends = friends - 1
print(friends)  # 10


# Augmeted assignment operator
friends += 1
print(friends)  # 11
friends -= 1
print(friends)  # 10
friends *= 2
print(friends)  # 20
friends /= 2
print(friends)  # 10.0


# a bit good ones
friends = 5
friends //= 2
print(friends)  # 2

friends %= 3
print(friends)  # 2 (remainder)

friends **= 5
print(friends)  # 32.0
