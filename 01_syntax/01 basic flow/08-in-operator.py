# in operator is also used to find the string 
# in literally means as it is in the grammar 
# (gives true or false) for boolean
# in looping loops through the string/collection/iterables
string = "I am trying to learn python for Data-Science"

print("learn: ", "learn" in string)
print("learning: ", "learning" in string)
print("python: ", "python" in string)


# python is case sensitive 
print("i", "i" in string)
print("I", "I" in string)
print("science", "science" in string)
print("Science", "Science" in string)

